using System;

namespace Calculator
{
    public class AllFunctions
    {
        public double Add(double a, double b)
        {
            return a  + b;
        }

        public double SubSubtraction(double a, double b)
        {
            return a - b;
        }

        public double Abs(double a)
        {
            return Math.Abs(a);
        }

        public double Miltiplication(double a, double b)
        {
            return a * b;
        }

    }
}
